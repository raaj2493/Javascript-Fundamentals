// Assignment Operators Examples in JavaScript

let x = 10;  // Simple assignment
console.log("Initial value:", x);

// 1. Addition assignment (+=)
x += 5;  // x = x + 5
console.log("After x += 5:", x);

// 2. Subtraction assignment (-=)
x -= 3;  // x = x - 3
console.log("After x -= 3:", x);

// 3. Multiplication assignment (*=)
x *= 2;  // x = x * 2
console.log("After x *= 2:", x);

// 4. Division assignment (/=)
x /= 4;  // x = x / 4
console.log("After x /= 4:", x);

// 5. Remainder assignment (%=)
x %= 3;  // x = x % 3
console.log("After x %= 3:", x);

// 6. Exponentiation assignment (**=)
x **= 4; // x = x ** 4
console.log("After x **= 4:", x);

// 7. Bitwise AND assignment (&=)
x = 5;   // Reset x to 5
x &= 3;  // x = x & 3
console.log("After x &= 3:", x);

// 8. Bitwise OR assignment (|=)
x |= 2;  // x = x | 2
console.log("After x |= 2:", x);

// 9. Bitwise XOR assignment (^=)
x ^= 1;  // x = x ^ 1
console.log("After x ^= 1:", x);

// 10. Left shift assignment (<<=)
x = 3;   // Reset x to 3
x <<= 2; // x = x << 2
console.log("After x <<= 2:", x);

// 11. Right shift assignment (>>=)
x >>= 1; // x = x >> 1
console.log("After x >>= 1:", x);

// 12. Unsigned right shift assignment (>>>=)
x >>>= 1; // x = x >>> 1
console.log("After x >>>= 1:", x);
