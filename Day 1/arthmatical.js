/* This code provides a quick overview of arithmetic operators in JavaScript. */

// We'll use a couple of variables for our examples.
let a = 15;
let b = 4;

// --- Basic Arithmetic Operations ---

// Addition (+)
console.log(`a + b = ${a + b}`); // Output: a + b = 19

// Subtraction (-)
console.log(`a - b = ${a - b}`); // Output: a - b = 11

// Multiplication (*)
console.log(`a * b = ${a * b}`); // Output: a * b = 60

// Division (/)
console.log(`a / b = ${a / b}`); // Output: a / b = 3.75

// Modulus (%) - returns the remainder of a division
console.log(`a % b = ${a % b}`); // Output: a % b = 3 (because 15 divided by 4 is 3 with a remainder of 3)

// Exponentiation (**) - raises the first operand to the power of the second
console.log(`a ** 2 = ${a ** 2}`); // Output: a ** 2 = 225

// --- Increment and Decrement Operators ---

// Increment (++) - adds one to the variable
let c = 5;
c++; // c is now 6
console.log(`After increment, c = ${c}`); // Output: After increment, c = 6

// Decrement (--) - subtracts one from the variable
let d = 10;
d--; // d is now 9
console.log(`After decrement, d = ${d}`); // Output: After decrement, d = 9